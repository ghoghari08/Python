# Integer data type
age = 30
print(type(age))

# Float data type
price = 19.99
print(type(price))

# String data type
name = "Hardik"
print(type(name))

# Boolean data type
is_active = True
print(type(is_active))

# List data type (ordered, mutable collection)
fruits = ["apple", "banana", "cherry"]
print(type(fruits))

# Tuple data type (ordered, immutable collection)
coordinates = (10, 20)
print(type(coordinates))

# Dictionary data type (unordered, key-value pairs)
person = {"name": "Hardik", "age": 30}
print(type(person))

# Set data type (unordered collection of unique elements)
unique_numbers = {1, 2, 3, 2, 4} # Duplicate '2' will be ignored
print(type(unique_numbers))